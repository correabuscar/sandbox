/// finds and emits cargo:rustc-link-lib=
fn find_library(names: &[&str]) -> Option<Library> {
    for name in names {
        //println!("cargo:warning=Trying lib '{}'",name);
        if let Ok(lib) = pkg_config::probe_library(name) {
            //println!("cargo:warning=Found lib '{}' '{:?}'",name, lib);
            return Some(lib);
        }
    }
    None
}
